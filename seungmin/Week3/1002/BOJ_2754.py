p = input()

if p == "A+":
    print("4.3")
elif p == "A0":
    print("4.0")
elif p == "A-":
    print("3.7")
elif p == "B+":
    print("3.3")
elif p == "B0":
    print("3.0")
elif p == "B-":
    print("2.7")
elif p == "C+":
    print("2.3")
elif p == "C0":
    print("2.0")
elif p == "C-":
    print("1.7")
elif p == "D+":
    print("1.3")
elif p == "D0":
    print("1.0")
elif p == "D-":
    print("0.7")
else:
    print("0.0")